'''
Exemple de commentaire.
Plusieurs lignes peuvent y etre ecrites.
'''
ma_variable = 5
print(type(ma_variable))  # <class 'int'>
ma_variable = 13.4
print(type(ma_variable))  # <class 'float'>
ma_variable = "une chaine de caractere"
print(type(ma_variable))  # <class 'str'>
ma_variable = None
print(type(ma_variable))  # <class 'NoneType'>
